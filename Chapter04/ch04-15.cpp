#include<iostream>
#include<string>
using namespace std;

int main() {
	cout << "여러 줄의 문자열을 입력하세요. 입력의 끝은 &문자입니다.\n";
	string s;
	getline(cin, s, '&');
	cin.ignore();

	string f, r;
	cout << endl << "find : ";
	getline(cin, f, '\n');
	cout << "replace : ";
	getline(cin, r, '\n');

	int startIndex = 0;
	while (true) {
		int index = s.find(f, startIndex);
		if (index == -1) break;
		s.replace(index, f.size(), r);
		startIndex = index + r.size();
	}
	cout << s << endl;
}